# Example 1 - Combining items, tuple assignment, and for loop to iterate through a list of tuples
# The output is in hash key order (i.e., no particular order).
# Create a dictionary d with key-value pairs.
e = {'a':10, 'b':1, 'c':22}
print('Example 1 - Combining items, tuple assignment, and for loop to iterate through a list of tuples')
# Iterate through the key-value pairs of the dictionary d,
# converting them to a list of tuples.
for key, val in list(e.items()):
    # Print the value followed by the key for each key-value pair.
    print(val, key)


